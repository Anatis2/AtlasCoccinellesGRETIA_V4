            <p>
                Chaque région est coordonnée par un ou plusieurs bénévoles, voire par des associations partenaires, dont certaines avaient déjà commencé à travailler sur les Coccinellidae bien avant l’idée d’un atlas pour le grand ouest.
                Aussi, il est important de remarquer que certains départements possédaient déjà de très nombreuses données avant le lancement de l'atlas :
                <ul>
                    <li>La Manche, avec la parution en 2003 de l’atlas des coccinelles de la Manche par l’association Manche-Nature ;</li>
                    <li>Le Maine-et-Loire, avec la publication en 2015 de « Les coccinelles de Maine-et-Loire » par l’association Anjou Nature ;</li>
                    <li>Les Côtes d’Armor, avec la page « coccinelles22 » du site « Nature22 », qui représente entre autre des cartes de répartition et des photos par espèce.</li>
                </ul>
            </p>

            <p>
                Vous trouverez les noms de nos différents coordinateurs ci-dessous, mais sachez d’ores et déjà que nous nous appuyons tous sur une seule et même adresse de contact : <strong>coccinelles@gretia.org</strong>.
                N’hésitez surtout pas à nous poser des questions ou nous envoyer des données de coccinelles (photographies, carnets de terrain scannés, tableurs, …). Nous nous efforcerons d’y répondre rapidement et du mieux possible.
            </p>

