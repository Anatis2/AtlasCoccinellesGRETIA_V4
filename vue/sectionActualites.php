            <div class="margeDiv coxDuMoisJuin">
                <h5>Juin</h5>

                <p>
                    La coccinelle de la bryone, <em>Henosepilachna argus</em>, est une espèce rouge-orangée pubescente et relativement grande (6-8mm). Elle est aisément identifiable et relativement commune sur sa plante hôte.
                    En effet, notre coccinelle étant phytophage et consommant exclusivement de la bryone, il faut cibler sa recherche, à vue ou en utilisant une nappe de battage, sur cette liane.
                </p>

                <p>
                    La Bryone (Bryonia dioica) est une plante grimpante qui se trouve notamment dans les haies, lisières de bois, fourrés ou même terrains vagues !
                    La coccinelle de la bryone semble par contre préférer les pieds biens exposés au soleil et est plus régulière en contexte urbain ou périurbain qu'en milieu bocager/agricole.
                    Si vous avez des melons dans votre jardin, n'hésitez pas à y jeter également un œil puisque c'est aussi un repas potentiel pour cette coccinelle (tout comme le cornichon !).
                </p>

                <p>
                    Le pic de contact de l'espèce observé en Pays de la Loire se situe nettement en juin mais il est également possible de trouver cette coccinelle plus tardivement en saison.
                    Outre la recherche de l'adulte à vue il ne faut pas hésiter à regarder les feuilles par transparence afin de détecter des zones claires indiquant que celle-ci a été "broutée" et laissant un indice devant motiver une inspection plus poussée sur le pied examiné.
                    Enfin les larves de cette espèces sont également assez facilement reconnaissables : claires avec des pics sur le dos.

                </p>

                <p>
                    Dans tous les cas n'oubliez pas de noter lors de vos observations de coccinelles dans le Grand Ouest (Normandie, Pays de la Loire, Bretagne) : la date, le lieu (commune, lieu-dit, coordonnées géographiques si possible) et le nom de l'observateur. Pour faire encore mieux, l'idéal est de rajouter : le support (sur quelle plante a été observée la coccinelle), un nombre d'individus ou toute autre information utile pour expliquer à terme la répartition des coccinelles sur notre territoire.
                </p>

