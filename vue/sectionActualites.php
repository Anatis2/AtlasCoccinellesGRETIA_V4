            <div class="margeDiv coxDuMoisOctobre">
                <h5>Octobre</h5>

                <p>
                    <em>Rodolia cardinalis</em> est une coccinelle importée d'Australie qui a été introduite en France pour lutter contre un ennemi de certaines plantes ornementales :
                    la cochenille australienne (<em>Icerya purchasi</em>).
                </p>

                <p>
                    Cette espèce se trouve donc particulièrement sur des plantes ornementales, et elle est à chercher activement sur les Orangers du Mexique (<em>Choisya ternata</em>).
                    Souvent utilisée dans les parterres décoratifs et haies des bourgs et villages, cette plante ornemmentale est en fleurs en ce moment.
                    Vous pouvez aussi faire un détour du côté des jardineries qui la vendent.
                    C'est en tout cas en ciblant cette plante que la coccinelle a été découverte à plusieurs reprises en Côte d'Armor il y a peu (Florence et Marc Cochu, obs., Vivarmor Nature). Une occasion de noter une nouvelle espèce pour bon nombre de département de l'Ouest de la France, nous ne l'avons pour l'instant que du 22 et du 85 !
                    D'autres essences comme le Mimosa ne sont pas à négliger et vous pouvez aussi y rencontrer d'autres espèces de coccinelles comme Lindorus forestieri. Toutes les données de larves et d'imagos sont bonnes à prendre !
                </p>

