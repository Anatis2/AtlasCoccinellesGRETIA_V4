            <div class="margeDiv coxDuMoisMars">
                <h5>Mars</h5>

                <p>
                    Le beau temps pointe timidement le bout de son nez, et il est temps de sortir : en mars, nous vous invitons à rechercher <em>Clitostethus arcuatus</em> !
                </p>

                <p>
                    C'est une des plus petites espèces de coccinelles. Elle n'en reste pas moins assez facile à identifier avec son arc clair dessiné sur ses élytres.
                    Si elle semble assez bien représentée au sud des Pays de la Loire elle apparaît, en l'état des connaissances, beaucoup plus rare au nord du Massif armoricain et en Normandie !
                </p>

                <p>
                    Son pic d'activité principal commence à partir de mi-février pour se poursuivre jusqu'à la mi-avril. C'est donc le moment idéal pour la rechercher !
                    Elle semble affectionner les lierres bien exposés au soleil fixés sur un support minéral, alors n'hésitez pas à aller battre cette végétation (avec un parapluie japonais,
                    ou juste en tendant un drap blanc dessous). Attention l'espèce reste très petite. Après avoir réalisé le battage d'une plante il faut bien inspecter sa nappe même si on ne voit rien bouger dans un premier temps.
                    L'espèce est assez rapide et, passée la chute et un temps d'immobilité, son déplacement sur la toile la rend plus facilement détectable ensuite.
                </p>

