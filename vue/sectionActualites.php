            <div class="margeDiv coxDuMoisMai">
                <h5>Mai</h5>

                <p>
                    <em>Oenopia doublieri</em>.<br/>
                    Pour la France, cette espèce n'était citée historiquement que de « la région méditerranéenne ; littoral de l'océan jusqu'à Rochefort » (Dauguet 1949, Coccinellini de France).
                    En 1969, Jean Chérel dans son ouvrage Buoc'haned Breizh (Les Coccinelles de Bretagne) cite cette espèce pour « la Loire-Atlantique.
                    Signalé du Maine-et-Loire...serait à rechercher surtout au bord de la mer. ».
                </p>

                <p>
                    En 2018, une visite dans les réserves du Muséum d'Histoire Naturelle de Nantes a permis de mieux documenter la présence historique de cette espèce pour les Pays de la Loire.
                    En effet, elle a pu être observée dans les boîtes de la très belle collection d'Henri Donnot qui a récolté des spécimens en bord de Loire ainsi que sur la côte vendéenne dans les années 1930.
                    De nos jours, en Vendée elle est présente et plutôt abondante sur presque toute la côte. Elle fréquente également les marais de Saint-Hilaire-de-Riez et le marais Breton.
                    On la trouve aussi sur l'île de Noirmoutier et l'île d'Yeu. Ces dernières années elle a pu être observée en Bretagne dans le Golfe du Morbihan.
                </p>

                <p>
                    Elle est à rechercher sur tamaris sur la côte et sur la végétation (saules, tamaris) des ripisylves des fleuves côtiers. En Vendée toutes les observations des imagos et des larves ont été faites sur tamaris.
                    Au battage de ses arbustes il faut reconnaître que l'on obtient plus facilement <em>Coniatus tamarisci</em> (Fabricius, 1787) le charançon du tamaris qui couvre rapidement toute la surface du parapluie japonais. Mais si parmi cet amas de charançons bigarrés un insecte un peu plus grand surgit, il pourrait bien alors s'agir de cette fameuse coccinelle que nous recherchons !
                </p>

