            <h4>La coccinelle du mois</h4>

            <div class="margeDiv coxDuMoisJanvier">
                <h5>Janvier</h5>

                <p>
                    L'hiver n'est pas la saison la plus propice pour les coccinelles. Le froid venant, certaines vont trouver refuge dans nos habitations.
                    L'occasion de faire des observations dans le cadre de l'atlas tout en restant au chaud ! Vous pouvez y observer les nombreuses formes de la coccinelle asiatique (Harmonia axyridis),
                    mais aussi d'autres espèces comme la coccinelle zig-zag, ou coccinelle rose (Oenopia conglobata), la coccinelle à deux points (Adalia bipunctata), ...
                </p>

                <p>
                    Dans vos maisons n'hésitez pas à aller inspecter les rainures des fenêtres, les pieds de vélux, le revers des rideaux proches de radiateurs, les angles des pièces, les combles et greniers, etc...
                    Dans tous les cas n'oubliez pas de noter lors de vos observations de coccinelles : la date, le lieu (commune, lieu-dit, coordonnées géographiques si possible) et le nom de l'observateur.
                    Envoyez vos photos et observations à coccinelles@gretia.org !
                </p>

