            <h3>Participer à l'atlas</h3>

                <p>Si vous souhaitez contribuer à l'atlas, il y a plusieurs possibilités !</p>

            <div class="margeDiv">
                <h4>Envoyer des données</h4>

                <p>
                    Vous pouvez nous envoyer des données, qu'elles soient sous forme de photos, carnets de terrain, tableurs, simple texte dans un mail, ...<br/>
                    La chose la plus importante à retenir est qu'une donnée est constituée au minimum de la commune où la coccinelle a été observée, du numéro de département, de la date d'observation et du nom de l'observateur.<br/><br/>
                    Par exemple, "une coccinelle à sept points observée le 15/05/2016 sur la commune de Vire (14) par Claire Coubard".<br/><br/>
                    Une localisation plus précise pourra être d'une aide préciseuse (coordonnées géographiques, nom du lieu-dit, ...).<br/>
                    Les données que chacun pourra nous apporter seront intégrées dans notre base de données, qui servira à effectuer une cartographie indicative de l'état de présence des espèces de coccinelles dans le grand ouest !<br/><br/>
                    Pour rappel, notre adresse mail est : coccinelles@gretia.org
                </p>
            </div>

