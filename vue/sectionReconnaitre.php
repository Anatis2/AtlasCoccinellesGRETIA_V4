            <h3>Reconnaître une coccinelle</h3>

            <p>
                Malgré leur très bonne côte de popularité, les coccinelles sont souvent exclusivement associées à la coccinelle à sept points.
                Or, il existe en France pas moins de 125 espèces de Coccinellidae et, contrairement à ce que disent les dictionnaires, les coccinelles ne sont pas obligatoirement des « petits insectes coléoptères aux élytres orangés ou rouges ornés de points noirs ».
                Tout sera une question d’espèce, voire d’individu…
            </p>

            <p>
                Par exemple :
                <ul>
                    <li><em>Psyllobora vigintiduopunctata</em>, parfois nommée la coccinelle marsupilami, est jaune à points noirs ;</li>
                    <li><em>Halyzia sedecimguttata</em>, la grande coccinelle orange, est orange à taches blanches ;</li>
                    <li><em>Chilocorus bipustulatus</em> est noire et rouge ;</li>
                    <li><em>Scymnus nigrinus</em> est une très petite coccinelle de couleur noire ;</li>
                    <li><em>Harmonia axyridis</em> peut revêtir des couleurs très différentes (c’est ce qu’on appelle des formes, ou des patterns).</li>
                </ul>
            </p>

            <figure>
                <img src="./images/ImagesCox/Halyzia-sedecimguttata.jpg" class="imageReduite" alt="Halyzia sedecimguttata"/>
                <figcaption>
                    <strong><em>Halyzia sedecimguttata</em></strong>
                </figcaption>
            </figure>

            <p>
                L’aspect hémisphérique et la taille ne sont pas des critères de détermination non plus.<br/>
                <em>Coccidula rufa</em>, la coccidule des marais, et <em>Hippodamia variegata</em>, ont un aspect très allongé.<br/>
                <em>Anatis ocellata</em>, la coccinelle ocellée, est l’une de nos plus grandes espèces (1cm) et doit ressembler à un géant pour <em>Stethorus pusillus</em>, qui ne dépasse pas 1mm !
            </p>

            <p>
                Certains insectes, notamment de la famille des Chrysomelidae peuvent ressembler très fortement aux Coccinellidae.
            </p>

            <p>
                De manière très précise, les critères discriminatoires des Coccinellidae par rapport aux autres familles d’invertébrés sont les suivants :
                <ul>
                    <li>dernier article des palpes maxillaires (organe faisant partie des pièces buccales) en forme de hache ;</li>
                    <li>antennes insérées à l’avant de la tête et terminées en massue ;</li>
                    <li>tarses (cinquième et dernière partie de la patte), sont composés de 4 articles (attention, le 3ème tarse est peu visible, mis à part pour le genre <em>Nephus</em>, dont les tarses n’ont réellement que 3 articles).</li>
                </ul>
            </p>

            <p>
                Mais pour la grande majorité des cas, les « coccinellistes » ne regardent pas ces critères et font plutôt jouer leur habitude d’observation.
            </p>

