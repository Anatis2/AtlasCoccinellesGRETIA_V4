            <h3>Biologie / écologie</h3>

            <div class="margeDiv cycleDeVie">
                <h4>Cycle de vie (exemple de la coccinelle à sept points)</h4>

                <p>
                    L’hiver, les adultes se mettent en diapause, c’est-à-dire qu’ils diminuent leur activité métabolique afin de perdre le moins d’énergie possible pendant la mauvaise saison.
                    Les coccinelles ne sortiront de cette léthargie qu’au printemps, et commenceront à chercher de la nourriture. Les pucerons n’étant pas vraiment présents en début de saison,
                    elles se nourriront de tout ce qu’elles trouvent à se mettre sous la dent (algues, collemboles, cochenilles, pollen, …).
                </p>

                <p>
                    C’est seulement après avoir repris suffisamment de forces qu’elles pourront se reproduire, souvent à partir du mois d’avril et jusqu’au mois de juin.
                    Les œufs sont pondus à proximité des colonies de pucerons afin que les larves puissent avoir un garde-manger à proximité dès la sortie de l’œuf, ce qui permet également d’éviter le cannibalisme intra-spécifique.
                    En général, les adultes meurent après avoir pondu. Les mois de juin et juillet correspondent donc principalement à la saison des larves.<br/>
                    Les coccinelles vont passer par 4 stades larvaires différents, et doivent accumuler suffisamment de réserves avant de pouvoir se transformer en nymphe, d’où sortira l’adulte au bout de quelques jours.
                </p>
            </div>

            <div class="margeDiv regimeAlimentaire">
                <h4>Régime alimentaire</h4>

                <p>
                    <em>Tout le monde sait que les coccinelles mangent des pucerons...</em>
                    <strong>Ce n'est pas faux, mais ce n'est pas totalement vrai non plus !</strong>
                </p>

                <p>
                    Il existe des espèces strictement phytophages, comme la coccinelle de la bryone. Dans le sud, cette dernière est appelée coccinelle du melon car elle y est considérée comme une espèce ravageuse ! <br/>
                    Certains Scymninae, comme <em>Stethorum pusillus</em> par exemple, seront spécialisés dans les acariens.<br/>
                    La grande coccinelle orange (<em>Halyzia sedecimguttata</em>) mangera quant à elle des champignons.
                </p>
            </div>

